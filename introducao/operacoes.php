<?php

// Operações matemáticas básicas com PHP

//SOMA
$num1 = 36;
$num2 = 3;

$soma = $num1 + $num2; // $soma recebe o valor 39 (36+3)

//SUBTRAÇÃO
$subtracao = $num1 - $num2; // $subtracao recebe o valor 33 (36-3)

//MULTIPLICAÇÃO
$multiplicacao = $num1 * $num2; // $multiplicação recebe o valor 108 (36 vezes 3)

//DIVISÃO
$divisao = $num1 / $num2; // $divisao == 12 (36 dividido por 3)

//RESTO
$resto = $num1 % $num2; // $resto recebe o resto da divisão de 36 por 3, ou seja, 0.

//POTÊNCIA
$potencia = 5 ** 3 // $potencia recebe o resultado de 3 elevado ao cubo (125).
